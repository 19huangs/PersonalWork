package objectOriented;

public class Circle {
	private double radius;

	public Circle() {
		radius = -999.99;

	}

	public double returnRadius() {
		return radius;
	}

	public double Circumfrence(double circumfrence) {
		return circumfrence;
	}

	public double Area(double area) {
		return area;
	}

	public void changeRadius(double xRadius) {
		radius = xRadius;
	}
}